#include "html_tag.h"

/**************************************************************************************
                   HTML_TAG::HTML_TAG
***************************************************************************************/

HTML_TAG::HTML_TAG()
{
}

/**************************************************************************************
                   HTML_TAG::~HTML_TAG
***************************************************************************************/

HTML_TAG::~HTML_TAG()
{
}

/**************************************************************************************
                   HTML_TAG::SET_TAG_NAME
***************************************************************************************/

void HTML_TAG::SET_TAG_NAME ( QString tag_name )
{
    p_tag_name = tag_name;
}

/**************************************************************************************
                   HTML_TAG::SET_ATTRIBUTE
***************************************************************************************/

void HTML_TAG::SET_ATTRIBUTE ( QString  attribute_name, QString attribute_value )
{
    p_attribute_name  = attribute_name;
    p_attribute_value = attribute_value;
}

/**************************************************************************************
                   HTML_TAG::SET_VALUE
***************************************************************************************/

void HTML_TAG::SET_VALUE ( QString value )
{
    p_value = value;
}

/**************************************************************************************
                   HTML_TAG::GET_HTML_TAG
***************************************************************************************/

QString HTML_TAG::GET_HTML_TAG()
{
    QString result;

    result.append ( QString ( "<%1 %2=%3>" ).arg ( p_tag_name, p_attribute_name, p_attribute_value ) );
    result.append ( p_value );
    result.append ( QString ( "</%1>" ).arg ( p_tag_name ) );

    return result;
}

































